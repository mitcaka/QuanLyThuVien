    private void LoadThongKeMuonTra(){      
        DefaultTableModel tableModel = new DefaultTableModel();
        tableModel.addColumn("Mã phiếu mượn");
        tableModel.addColumn("Mã độc giả");
        tableModel.addColumn("Tên độc giả");                
        tableModel.addColumn("Ngày mượn"); 
        tableModel.addColumn("Hạn mượn");   
        tableModel.addColumn("Ghi chú");           
        tableModel.addColumn("Tình trạng");           
        
        List<DocGia> lstDG = dgDAO.getAllDocGia();
        List<PhieuMuon> lstPM = pmDAO.getPhieuMuon();
        for(PhieuMuon sp : lstPM){
            String TinhTrang = "";
            if(sp.getTrangThai() == 1){
                TinhTrang = "Chưa trả";
            }else if(sp.getTrangThai() == 2){
                TinhTrang = "Đã trả";
            }else{
                TinhTrang = "Chưa mượn";
            }
            
            String TenDG = "";
            for(DocGia dg : lstDG){
                if(dg.getMaDocGia().equals(sp.getMaDocGia())) TenDG = dg.getHoTen();
            }      
            tableModel.addRow(new Object[]{sp.getMaPhieuMuon(),sp.getMaDocGia(),TenDG,sp.getNgayMuon(),sp.getHanMuon(),sp.getGhiChu(),TinhTrang});
        }
        tblMuonTra.setModel(tableModel);        
    }
    
