    private void LoadLabel(){
        List<ChiTietPhieuMuon> lstCT = ctDAO.getChiTietPhieuMuon();
        if(lstCT!=null){
            int soPhieuPhat = 0;
            float TongTienPhat = 0;
            int SachHong = 0;
            int SachMat = 0;
            for(ChiTietPhieuMuon ct : lstCT){
                if(ct.getMucPhat()!=0){
                    soPhieuPhat++;
                    TongTienPhat+=ct.getMucPhat();
                }
                
                if(ct.getTinhTrangSach().equals("Hư hỏng")){
                    SachHong++;
                }
                
                if(ct.getTinhTrangSach().equals("Mất")){
                    SachMat++;
                }
            }
            lblTongTienPhat.setText(chuyentien.format(TongTienPhat) + " VNĐ");
            lblPhieuPhat.setText(String.valueOf(soPhieuPhat));
            lblSachHong.setText(String.valueOf(SachHong));
            lblSachMat.setText(String.valueOf(SachMat));
            
        }        
        List<Sach> lstSach = sDAO.getSach();
        lblTongSach.setText(String.valueOf(lstSach.size()));
    }
    
