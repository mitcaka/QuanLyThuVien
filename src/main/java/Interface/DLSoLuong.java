    private void Fill(){
        DauSach ds = dsDAO.getDauSachByMa(maDS);
        lblNamXB.setText(ds.getNamXuatBan());
        lblNhaXB.setText(ds.getNhaXuatBan());
        lblTacGia.setText(ds.getTacGia());
        lblTenDS.setText(ds.getTenSach());
        lblSL.setText(Integer.toString(ds.getSoLuong()));
        txtMoTa.setText(ds.getMoTa());
        
        List<Sach> listSach = sDAO.getSachTrangThai1ByMaDS(maDS);
        DefaultListModel<String> l1 = new DefaultListModel<>(); 
        for(Sach i : listSach){
            l1.addElement(Integer.toString(i.getMaS()));
        }
        
        jListMSS.setModel(l1);
    }
    
    
