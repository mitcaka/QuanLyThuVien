    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        List<SachMuon> lstSach = new ArrayList<SachMuon>();
        List<String> lstMaSach = jListMSS.getSelectedValuesList();
        
        for(String i : lstMaSach){
            SachMuon s = new SachMuon();
            s.setMaDS(maDS);
            s.setTenSach(lblTenDS.getText());
            s.setMaSach(Integer.parseInt(i));
            lstSach.add(s);
            sDAO.OffSach(Integer.parseInt(i));
        }
        father.LoadSachMuon(lstSach);
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

